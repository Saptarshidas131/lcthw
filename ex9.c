// include standard input/output header
#include <stdio.h>

// main function with command line arguments
int main(int argc, char *argv[])
{
    // set i to 0
    int i = 0;
    
    // loop as long as i is less than 25
    while(i < 25)
    {
        // print the value of i
        printf("%d ",i);
        // increment i by 1
        i++;
    }
    // return from main
    return 0;
}
